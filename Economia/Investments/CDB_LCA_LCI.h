/*
# 1. CDB (Certificado de Depósito Bancário)
	Você empresta dinheiro ao banco, e ele devolve com juros.
	É o mais comum entre os três.
	-Rentabilidade:
		Pode ser pós-fixado (ex: 110% do CDI), prefixado ou IPCA+.
	-Exemplo:
		110% do CDI → atualmente rende mais que a poupança (~1,1% ao mês).
	-Garantia:
		Coberto pelo FGC (Fundo Garantidor de Créditos) até R$ 250 mil por CPF por instituição.
	-Liquidez:
		Pode ter liquidez diária (ideal para reserva de emergência), ou só no vencimento.

# 2. LCI (Letra de Crédito Imobiliário)
# 3. LCA (Letra de Crédito do Agronegócio)
	O que são?
	Também são títulos de dívida do banco, mas voltados para:

	LCI: setor imobiliário
	LCA: setor do agronegócio

	-Isenção de IR:
		Isentos de Imposto de Renda para pessoa física!
		Isso pode torná-los mais vantajosos que CDBs com mesmo rendimento bruto.
	-Rentabilidade:
		Pós-fixada (% do CDI), prefixada ou híbrida (ex: IPCA + taxa).
	-Exemplo:
		LCI a 93% do CDI líquido pode superar um CDB a 110% do CDI com IR.
	-Garantia:
		Também cobertos pelo FGC (até R$ 250 mil por CPF por instituição).
	-Liquidez:
		Em geral, sem liquidez diária. Prazos de carência comuns: 90, 180 ou 360 dias.

__________________________________________________________________________________
|                     Comparativo prático:                                        |
|_________________________________________________________________________________|
|Característica		|CDB				|LCI				 |LCA                 |
|IR					|Sim (15% a 22,5%)	|Não				 |Não                 |
|Garantia FGC		|Sim				|Sim				 |Sim                 |
|Liquidez diária	|Depende			|Raro				 |Raro                |
|Valor mínimo		|A partir de R$1	|A partir de R$ 1000 |A partir de R$ 1000 |
|Uso dos recursos	|Banco geral		|Crédito imobiliário |Crédito agropecuário|
|___________________|___________________|____________________|____________________|

	-Dicas para escolher:
		Use CDB com liquidez diária para reserva de emergência.
		Use LCI/LCA para planos de médio prazo e sem necessidade de resgatar antes.
		Compare sempre o rendimento líquido:
		CDB com IR pode render menos que LCI/LCA mesmo com percentual maior do CDI.
*/