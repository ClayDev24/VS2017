/*
1. Tesouro Direto (Governo):
	-O que é: Você empresta dinheiro para o governo, que devolve com juros depois.
	-Tipos principais:
		-Tesouro Selic: Rende próximo à taxa Selic (ideal para reserva de emergência).
		-Tesouro IPCA+: Protege contra inflação (bom para longo prazo).
		-Tesouro Prefixado: Juros fixos (bom se os juros caírem no futuro).

	Exemplo:
	Você investe R$ 1.000 no Tesouro IPCA+ a 6% ao ano + inflação por 5 anos.


2. CDB, LCI e LCA (Bancos)
	-CDB (Certificado de Depósito Bancário): Você empresta dinheiro ao banco.
		-Pode render 100% do CDI ou mais.
		-Tem imposto de renda regressivo.
	-LCI/LCA: Parecidos com CDB, mas isentos de IR para pessoa física.
		-LCI: Ligada ao setor imobiliário.
		-LCA: Ligada ao agronegócio.

	Exemplo:
	Um CDB que paga 110% do CDI rende mais que a poupança e pode ser resgatado em 1 ano.

3. Ações
	-O que são: Você se torna sócio de uma empresa da Bolsa de Valores.
	-Ganhos:
		-Valorização das ações (compra a R$ 10, vende a R$ 15).
		-Dividendos: parte do lucro que a empresa paga ao investidor.

	Exemplo:
	Você compra 100 ações da Petrobras a R$ 30 e recebe R$ 300 em dividendos no ano.

4. Fundos Imobiliários (FIIs)
	-O que são: Você compra “pedacinhos” de imóveis (shopping, galpões, etc.).
	-Vantagens:
		-Renda mensal isenta de IR.
		-Liquidez maior que imóveis físicos.

	Exemplo:
	Você compra cotas de um FII que paga R$ 0,60 por mês por cota. Com 100 cotas, recebe R$ 60 por mês.

	5. Fundos de Investimento
	-O que são: Um gestor aplica seu dinheiro em vários ativos.
	-Tipos:
		-Renda fixa
		-Multimercado
		-Ações
		-Cambial

	Cuidado: Fundos têm taxa de administração e às vezes taxa de performance.

6. Criptomoedas
	-O que são: Moedas digitais como Bitcoin, Ethereum etc.
	-Alta volatilidade: Podem subir ou cair muito em pouco tempo.
	-Risco: Maior, mas com potencial de altos retornos.

	Exemplo:
	Bitcoin valorizou +300% entre 2020 e 2021, mas também pode cair 50% em semanas.


7. Dicas para começar
	1-Tenha reserva de emergência (Tesouro Selic ou CDB com liquidez diária).
	2-Entenda seu perfil de investidor: conservador, moderado ou arrojado.
	3-Invista de forma diversificada.
	4-Estude sempre: Use simuladores e plataformas como Tesouro Direto, NuInvest, XP, etc.
*/